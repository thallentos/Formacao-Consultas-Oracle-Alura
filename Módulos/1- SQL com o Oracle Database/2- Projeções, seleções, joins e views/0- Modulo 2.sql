-- Usando IN
SELECT *
FROM TABELA_DE_CLIENTES
WHERE CIDADE IN ('Rio de Janeiro', 'São Paulo')
    AND (IDADE BETWEEN 20 AND 25);
    
SELECT *
FROM TABELA_DE_CLIENTES
WHERE BAIRRO IN
